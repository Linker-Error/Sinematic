// Font loading function: IntroFont
static Font LoadFont_IntroFont(void)
{
    Font font = { 0 };

    font.baseSize = 32;
    font.glyphCount = 95;
    font.glyphPadding = 4;

    // Custom font loading
    // NOTE: Compressed font image data (DEFLATE), it requires DecompressData() function
    int fontDataSize_IntroFont = 0;
    unsigned char *data = DecompressData(fontData_IntroFont, COMPRESSED_DATA_SIZE_FONT_INTROFONT, &fontDataSize_IntroFont);
    Image imFont = { data, 512, 512, 1, 2 };

    // Load texture from image
    font.texture = LoadTextureFromImage(imFont);
    UnloadImage(imFont);  // Uncompressed data can be unloaded from memory

    // Assign glyph recs and info data directly
    // WARNING: This font data must not be unloaded
    font.recs = fontRecs_IntroFont;
    font.glyphs = fontGlyphs_IntroFont;

    return font;
}
